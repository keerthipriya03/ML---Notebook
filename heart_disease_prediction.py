import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

url = "https://raw.githubusercontent.com/kb22/Heart-Disease-Prediction/master/dataset.csv"

heart_data = pd.read_csv(url)

heart_data.head()

heart_data.shape

heart_data.info     #reference to the method itself in a Pandas DataFrame.It's just points to the function.

heart_data.info()      #calls the method. It prints summary information about the DataFrame.

# checking for missing values
heart_data.isnull().sum()

# statistical measures about the data
heart_data.describe()

# checking the distribution of Target Variable
heart_data['target'].value_counts()                #1->defective    ,   0->Healthy

#Splitting the features and the target
x = heart_data.drop(columns='target' , axis=1)
y = heart_data['target']

print(x)
print(y)

print(x,y)

#splitting datasets
X_train, X_test, Y_train, Y_test = train_test_split(x, y, test_size=0.2, stratify=y, random_state=2)

"""Note:   stratify is applied only to Y, the target labels
  - The purpose of stratified sampling is to preserve the proportion of each class in the target variable in both the training and test sets.
  - X (features) is split according to how Y is split, so the features corresponding to each label go along with it automatically.

Ex: Y_test might be as  [0,1,1]  ,  then Y_train will remain as [0,1,1]
"""

print(x.shape, X_train.shape, X_test.shape)

#Logistic Regression model training
model = LogisticRegression()

# training the LogisticRegression model with Training data
model.fit(X_train , Y_train)

#Model evaluation - Accuracy score

# accuracy on training data
X_train_prediction = model.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)

print('Accuracy on Training data : ', training_data_accuracy)

# accuracy on test data
X_test_prediction = model.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)

print("Accuracy on Test Data: " ,test_data_accuracy)

#Build a predictive system
input_data = (62,0,0,140,268,0,0,160,0,3.6,0,2,2)

# change the input data to a numpy array
input_data_as_numpy_array= np.asarray(input_data)

# reshape the numpy array as we are predicting for only on instance
input_data_reshaped = input_data_as_numpy_array.reshape(1,-1)

prediction = model.predict(input_data_reshaped)
print(prediction)

if (prediction[0]== 0):
  print('The Person does not have a Heart Disease')
else:
  print('The Person has Heart Disease')

